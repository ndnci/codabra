/**
 * Generates (or regenerates) all Docker config files:
 *   docker/<providerName>/Dockerfile   ← provider-specific, from provider.generateDockerfile()
 *   docker/docker-compose.dev.yml      ← DB services only (app runs locally)
 *   docker/docker-compose.prod.yml     ← full stack (app + DBs)
 *   Makefile                           ← dev-up/down/restart/build + prod-*
 *
 * Safe to call multiple times — always overwrites with the current provider config state.
 */
export function generateDockerConfig(
    projectDir: string,
    projectName: string,
    providerConfigs: ProviderConfig[],
): void {
    const databases = [...new Set(providerConfigs.map((c) => c.database))];
    const dockerDir = path.join(projectDir, "docker");

    // Per-provider Dockerfiles
    const uniqueProviders = [...new Set(providerConfigs.map((c) => c.provider))];
    for (const providerName of uniqueProviders) {
        try {
            const provider = getProvider(providerName).create();
            const dockerfileContent = provider.generateDockerfile();
            writeFile(path.join(dockerDir, providerName, "Dockerfile"), dockerfileContent);
        } catch {
            // Unknown provider — skip Dockerfile generation rather than crashing
        }
    }

    writeFile(path.join(dockerDir, "docker-compose.dev.yml"), generateDevCompose(projectName, databases));
    writeFile(path.join(dockerDir, "docker-compose.prod.yml"), generateProdCompose(projectName, providerConfigs));
    writeFile(path.join(projectDir, "Makefile"), generateMakefile());
}

/**
 * Returns true if the dev compose file in projectDir contains actual DB services
 * (i.e., any provider uses a non-SQLite database).
 */
export function hasDockerDevServices(projectDir: string): boolean {
    const composePath = path.join(projectDir, "docker", "docker-compose.dev.yml");
    if (!fs.existsSync(composePath)) return false;
    const content = fs.readFileSync(composePath, "utf-8");
    return content.includes("image:");
}

