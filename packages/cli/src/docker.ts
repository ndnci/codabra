import { execSync } from "child_process";

/** Returns true if the `docker` binary is available on the system. */
export function isDockerInstalled(): boolean {
    try {
        execSync("docker --version", { stdio: "ignore" });
        return true;
    } catch {
        return false;
    }
}

/**
 * Detects the current OS platform for Docker installation guidance.
 * Returns "linux" | "macos" | "windows".
 */
export function detectPlatform(): "linux" | "macos" | "windows" {
    const p = process.platform;
    if (p === "darwin") return "macos";
    if (p === "win32") return "windows";
    return "linux";
}

/**
 * Attempts to install Docker on Linux using the official convenience script.
 * Throws on failure. Only call after user confirmation.
 */
export function installDockerLinux(): void {
    execSync("curl -fsSL https://get.docker.com | sh", { stdio: "inherit" });
}

const DOCKER_INSTALL_URL = "https://docs.docker.com/get-docker/";

/**
 * If the chosen database requires Docker (i.e. non-SQLite), checks whether
 * Docker is installed and offers to install it interactively.
 *
 * `confirm` must be passed in to avoid importing @inquirer/prompts here
 * (keeps this module framework-agnostic and easier to test).
 */
export async function ensureDockerIfNeeded(
    database: string,
    confirmFn: (opts: { message: string; default?: boolean }) => Promise<boolean>,
): Promise<void> {
    if (database === "sqlite") return; // SQLite is a local file — no Docker needed

    if (isDockerInstalled()) return; // Already installed — nothing to do

    const platform = detectPlatform();

    console.log("");
    console.log("  Docker is required to run the database service locally.");
    console.log(`  Install guide: ${DOCKER_INSTALL_URL}`);
    console.log("");

    if (platform === "linux") {
        const install = await confirmFn({
            message: "Docker is not installed. Install it now via the official script? (curl -fsSL https://get.docker.com | sh)",
            default: true,
        });
        if (install) {
            try {
                installDockerLinux();
            } catch {
                console.log("  Docker installation failed. Please install manually and re-run.");
            }
        } else {
            console.log(`  Skipping Docker install. Run 'make dev-up' after installing it from ${DOCKER_INSTALL_URL}`);
        }
    } else if (platform === "macos") {
        console.log("  On macOS, install Docker Desktop or run: brew install --cask docker");
        console.log(`  Then run 'make dev-up' to start your database.`);
    } else {
        console.log("  On Windows, download Docker Desktop from:");
        console.log(`  ${DOCKER_INSTALL_URL}`);
        console.log(`  Then run 'make dev-up' to start your database.`);
    }
    console.log("");
}
