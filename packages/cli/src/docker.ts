// ─────────────────────────────────────────────
// File helpers
// ─────────────────────────────────────────────

function writeFile(filePath: string, content: string): void {
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, content, "utf-8");
}

// ─────────────────────────────────────────────
// Per-database service blocks
// ─────────────────────────────────────────────

function postgresDevService(projectName: string): string {
    return [
        `  postgres:`,
        `    image: postgres:16-alpine`,
        `    restart: unless-stopped`,
        `    environment:`,
        `      POSTGRES_DB: ${projectName}_dev`,
        `      POSTGRES_USER: postgres`,
        `      POSTGRES_PASSWORD: postgres`,
        `    ports:`,
        `      - "5432:5432"`,
        `    volumes:`,
        `      - postgres_dev_data:/var/lib/postgresql/data`,
    ].join("\n");
}

function postgresProdService(projectName: string): string {
    return [
        `  postgres:`,
        `    image: postgres:16-alpine`,
        `    restart: unless-stopped`,
        `    environment:`,
        `      POSTGRES_DB: \${POSTGRES_DB:-${projectName}_prod}`,
        `      POSTGRES_USER: \${POSTGRES_USER:-postgres}`,
        `      POSTGRES_PASSWORD: \${POSTGRES_PASSWORD:?POSTGRES_PASSWORD is required}`,
        `    volumes:`,
        `      - postgres_prod_data:/var/lib/postgresql/data`,
    ].join("\n");
}

function mysqlDevService(projectName: string): string {
    return [
        `  mysql:`,
        `    image: mysql:8.0`,
        `    restart: unless-stopped`,
        `    environment:`,
        `      MYSQL_DATABASE: ${projectName}_dev`,
        `      MYSQL_ROOT_PASSWORD: root`,
        `    ports:`,
        `      - "3306:3306"`,
        `    volumes:`,
        `      - mysql_dev_data:/var/lib/mysql`,
    ].join("\n");
}

function mysqlProdService(projectName: string): string {
    return [
        `  mysql:`,
        `    image: mysql:8.0`,
        `    restart: unless-stopped`,
        `    environment:`,
        `      MYSQL_DATABASE: \${MYSQL_DATABASE:-${projectName}_prod}`,
        `      MYSQL_ROOT_PASSWORD: \${MYSQL_ROOT_PASSWORD:?MYSQL_ROOT_PASSWORD is required}`,
        `    volumes:`,
        `      - mysql_prod_data:/var/lib/mysql`,
    ].join("\n");
}

/** App service block in prod compose — references the provider-specific Dockerfile */
function appProdService(providerName: string, databases: string[]): string {
    const dependsOn = databases.filter((db) => db === "postgresql" || db === "mysql");

    const lines = [
        `  ${providerName}:`,
        `    build:`,
        `      context: ..`,
        `      dockerfile: docker/${providerName}/Dockerfile`,
        `    restart: unless-stopped`,
        `    ports:`,
        `      - "\${PORT:-3000}:3000"`,
        `    environment:`,
        `      NODE_ENV: production`,
        `      DATABASE_URL: \${DATABASE_URL:?DATABASE_URL is required}`,
    ];

    if (dependsOn.length > 0) {
        lines.push(`    depends_on:`);
        for (const db of dependsOn) {
            lines.push(`      - ${db === "postgresql" ? "postgres" : "mysql"}`);
        }
    }

    return lines.join("\n");
}

// ─────────────────────────────────────────────
// Volume declarations
// ─────────────────────────────────────────────

function devVolumes(databases: string[]): string {
    const volumes: string[] = [];
    if (databases.includes("postgresql")) volumes.push("  postgres_dev_data:");
    if (databases.includes("mysql")) volumes.push("  mysql_dev_data:");
    return volumes.length > 0 ? `\nvolumes:\n${volumes.join("\n")}\n` : "";
}

function prodVolumes(databases: string[]): string {
    const volumes: string[] = [];
    if (databases.includes("postgresql")) volumes.push("  postgres_prod_data:");
    if (databases.includes("mysql")) volumes.push("  mysql_prod_data:");
    return volumes.length > 0 ? `\nvolumes:\n${volumes.join("\n")}\n` : "";
}

// ─────────────────────────────────────────────
// Compose file generators
// ─────────────────────────────────────────────

function generateDevCompose(projectName: string, databases: string[]): string {
    const serverDbs = databases.filter((db) => db !== "sqlite");
    const lines = [
        `# Auto-generated by Codabra — do not edit manually`,
        `# Dev: database services only (app runs locally for hot-reload)`,
        ``,
    ];

    if (serverDbs.length === 0) {
        lines.push(`# SQLite requires no Docker service — the database is a local file.`);
        lines.push(`# This file is intentionally empty for SQLite-only projects.`);
        lines.push(`services: {}`);
        return lines.join("\n") + "\n";
    }

    lines.push(`services:`);
    for (const db of serverDbs) {
        if (db === "postgresql") lines.push(postgresDevService(projectName));
        if (db === "mysql") lines.push(mysqlDevService(projectName));
        lines.push(``);
    }

    return lines.join("\n") + devVolumes(serverDbs);
}

function generateProdCompose(projectName: string, providerConfigs: ProviderConfig[]): string {
    const databases = [...new Set(providerConfigs.map((c) => c.database))];
    const serverDbs = databases.filter((db) => db !== "sqlite");
    const providerNames = [...new Set(providerConfigs.map((c) => c.provider))];

    const lines = [
        `# Auto-generated by Codabra — do not edit manually`,
        `# Prod: full stack — set DATABASE_URL and DB passwords via environment or .env`,
        ``,
        `services:`,
    ];

    for (const db of serverDbs) {
        if (db === "postgresql") lines.push(postgresProdService(projectName));
        if (db === "mysql") lines.push(mysqlProdService(projectName));
        lines.push(``);
    }

    for (const providerName of providerNames) {
        lines.push(appProdService(providerName, databases));
        lines.push(``);
    }

    return lines.join("\n") + prodVolumes(serverDbs);
}

function generateMakefile(): string {
    const tab = "\t";
    return [
        `.PHONY: dev-up dev-down dev-restart dev-build prod-up prod-down prod-restart prod-build`,
        ``,
        `dev-up:`,
        `${tab}docker compose -f docker/docker-compose.dev.yml up -d`,
        ``,
        `dev-down:`,
        `${tab}docker compose -f docker/docker-compose.dev.yml down`,
        ``,
        `dev-restart:`,
        `${tab}docker compose -f docker/docker-compose.dev.yml restart`,
        ``,
        `dev-build:`,
        `${tab}docker compose -f docker/docker-compose.dev.yml build`,
        ``,
        `prod-up:`,
        `${tab}docker compose -f docker/docker-compose.prod.yml up -d`,
        ``,
        `prod-down:`,
        `${tab}docker compose -f docker/docker-compose.prod.yml down`,
        ``,
        `prod-restart:`,
        `${tab}docker compose -f docker/docker-compose.prod.yml restart`,
        ``,
        `prod-build:`,
        `${tab}docker compose -f docker/docker-compose.prod.yml build`,
        ``,
    ].join("\n");
}

