// SQLite dialect
// ─────────────────────────────────────────────

const sqliteDialect: DrizzleDialectConfig = {
    tableFactory: "sqliteTable",
    corePackage: "drizzle-orm/sqlite-core",
    typeMap: {
        string: { fn: "text", import: "text" },
        number: { fn: "integer", import: "integer" },
        boolean: { fn: "integer", import: "integer" }, // SQLite has no boolean — stored as 0/1
        date: { fn: "integer", import: "integer" }, // stored as epoch ms
    },
    uuidImport: "text",
    dateNowImport: "integer",
    fkExpr: (col) => `text('${col}')`,
    idPkExpr: (col) => `text('${col}').primaryKey().$defaultFn(() => crypto.randomUUID())`,
    uuidExpr: (col) => `text('${col}').$defaultFn(() => crypto.randomUUID())`,
    dateNowExpr: (col) => `integer('${col}').$defaultFn(() => Date.now())`,
    generateClientFile: () =>
        [
            `// Auto-generated by Codabra — do not edit manually`,
            `import Database from 'better-sqlite3';`,
            `import { drizzle } from 'drizzle-orm/better-sqlite3';`,
            `import * as schema from '@/drizzle/schema';`,
            ``,
            `const sqlite = new Database('sqlite.db');`,
            `export const db = drizzle(sqlite, { schema });`,
            ``,
        ].join("\n"),
    getDependencies: () => ({ "drizzle-orm": "^0.30.0", "better-sqlite3": "^9.0.0" }),
    getDevDependencies: () => ({ "drizzle-kit": "^0.21.0", "@types/better-sqlite3": "^7.6.0" }),
};

