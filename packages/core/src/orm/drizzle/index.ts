// ─────────────────────────────────────────────
// Dialect config interface
// ─────────────────────────────────────────────

interface DrizzleTypeEntry {
    fn: string;
    import: string;
    /** Extra arguments after the column name, e.g. "{ length: 255 }" for MySQL varchar */
    extraArgs?: string;
}

interface DrizzleDialectConfig {
    readonly tableFactory: string;
    readonly corePackage: string;
    /** Type map for simple field types (string, number, boolean, date) */
    readonly typeMap: Record<string, DrizzleTypeEntry>;
    /** Import name used for UUID and FK columns */
    readonly uuidImport: string;
    /** Import name used for date_now columns */
    readonly dateNowImport: string;
    /** FK column expression (no default value) */
    fkExpr(colName: string): string;
    /** Primary key id expression (with primaryKey + auto-generated default) */
    idPkExpr(colName: string): string;
    /** UUID field expression (with auto-generated default) */
    uuidExpr(colName: string): string;
    /** date_now field expression (with auto-set default) */
    dateNowExpr(colName: string): string;
    /** Content of the generated ORM client file */
    generateClientFile(): string;
    getDependencies(): Record<string, string>;
    getDevDependencies(): Record<string, string>;
}

