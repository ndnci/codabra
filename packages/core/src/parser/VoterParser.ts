import type { VoterDefinition, VoterRule, VoterCondition } from "../types";

// ─────────────────────────────────────────────
// Condition expression generation
// ─────────────────────────────────────────────

function generateConditionExpr(condition: VoterCondition | undefined): string {
    if (!condition) return "true";

    if (condition.self) {
        // Resource ownership: the authenticated user must own the resource
        return `resource != null && (resource as Record<string, unknown>)['userId'] === userId`;
    }

    if (condition.field && condition.operator !== undefined) {
        const lhs = `(resource as Record<string, unknown>)['${condition.field}']`;
        const rhs = JSON.stringify(condition.value);
        return `resource != null && ${lhs} ${condition.operator} ${rhs}`;
    }

    return "true";
}

// ─────────────────────────────────────────────
// Rule block generation
// ─────────────────────────────────────────────

function generateRuleBlock(rule: VoterRule, pad: string): string[] {
    const lines: string[] = [];
    const methodsExpr = JSON.stringify(rule.methods);
    const condExpr = generateConditionExpr(rule.condition);

    lines.push(`${pad}if (${methodsExpr}.includes(method as never)) {`);
    if (condExpr === "true") {
        lines.push(`${pad}  return true;`);
    } else {
        lines.push(`${pad}  return ${condExpr};`);
    }
    lines.push(`${pad}}`);
    return lines;
}

